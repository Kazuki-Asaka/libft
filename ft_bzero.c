#include "libft.h"

void	ft_bzero(void	*s, size_t	n)
{
	size_t	i;
	char	*tmp;

	i = 1;
	tmp = s;
	while (i <= n)
	{
		*tmp = '\0';
		tmp++;
		i++;
	}
}
